# Mostrando as variáveis - tipo primitivo

a = input('Digita ai o que tu quiser: ')

print('O tipo primitivo disso ai é ', type(a))

print('Só tem espaços? ', a.isspace())
print('É um número? ', a.isnumeric())
print('É alfabético? ', a.isalpha())
print('Alphanumerico? ', a.isalnum())
print('Letras Maiusculas? ', a.isupper())
print('Letras minusculas? ', a.islower())
print('Esta capitalizada? ', a.istitle())


